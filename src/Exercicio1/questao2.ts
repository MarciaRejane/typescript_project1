// Podem ser atualizada e declaradas novamente dentro de seu escopo.
var nome1: string = "José"

// Podem ser atualizadas mas não declaradas novamente.
let nome2: string = "Maria";

// Não podem ser declaradas novamente e nem atualizadas, elas são imutaveis.
const numero: number = 80;

console.log(nome1);
console.log(nome2);
console.log(numero);